                eregion_realms_start = 21
            end
        end
    elseif eregion_realms_start == 18 then
        if eur_turn_number > 50 then
            show_kon_choice = true
        end
    elseif eregion_realms_start == 19 then
        formKoN()
        eur_eregion_active = false
        eregion_realms_start = 20
        options_poe = false
    elseif eregion_realms_start == 21 then
        local settlement = eur_sMap:getSettlement("Eregion")
        if settlement ~= nil then
            if settlement:buildingPresentMinLevel("high_king_throne", true) then
                --eregion story end
                local name = eur_campaign:getFaction("egypt").leader.localizedDisplayName
