function swapKoNBarracks()
    local faction = eur_player_faction
    for i = 0, faction.settlementsNum - 1 do
        local sett = faction:getSettlement(i)
        if sett ~= nil then
            local replace = false
            if eur_player_faction.name == "saxons" then
                if sett.isCastle == 0 then
                    if sett:buildingPresent("teleri_barracks") then
                        sett:destroyBuilding("teleri_barracks",false);
                        replace = true
                    end
                    if replace then
                        if not sett:buildingPresent("noldor_barracks") then
                            --make new barracks t1
                            sett:createBuilding("noldor_barracks")
                        end
                    end
                else
                    if sett:buildingPresent("teleri_barracks_castle") then
                        sett:destroyBuilding("teleri_barracks_castle",false);
                        replace = true
                    end
                    if replace then
                        if not sett:buildingPresent("noldor_barracks_castle") then
                            --make new barracks t1
                            sett:createBuilding("c_noldor_barracks")
                        end
                    end
                end
            else
                if sett.isCastle == 0 then
                    if sett:buildingPresent("noldor_barracks") then
                        sett:destroyBuilding("noldor_barracks",false);
                        replace = true
                    end
                    if replace then
                        if not sett:buildingPresent("teleri_barracks") then
                            --make new barracks t1
                            sett:createBuilding("teleri_barracks")
                        end
                    end
                else
                    if sett:buildingPresent("noldor_barracks_castle") then
                        sett:destroyBuilding("noldor_barracks_castle",false);
                        replace = true
                    end
                    if replace then
                        if not sett:buildingPresent("teleri_barracks_castle") then
                            --make new barracks t1
                            sett:createBuilding("c_teleri_barracks")
                        end
                    end
                end
            end
        end
    end
