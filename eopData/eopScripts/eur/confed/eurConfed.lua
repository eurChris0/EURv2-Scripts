function respawnChar(char, faction, x, y)
    if char == nil then return end
    if faction == nil then return end
    local temp_list = {
        name = char.characterRecord.localizedDisplayName,
        bg = char.bodyguards,
        exp = char.bodyguards.exp,
        traits = {},
        anc = {},
        portrait = char.characterRecord.portrait
    }
    for x = 0, char.characterRecord.ancNum - 1 do
        local anc = char.characterRecord:getAncillary(x)
        if anc ~= nil then
            table.insert(temp_list.anc, anc.name)
        end
    end
    char:kill()
    local army = eurSpawnArmy(faction.name, temp_list.name, temp_list.name..tostring(eur_turn_number).."re", temp_list.name, false, 30, temp_list.bg, x, y, temp_list.exp, 0, 0)
    if army ~= nil then
        if army.leader ~= nil then
            for i = 1, #temp_list.anc do
                army.leader.characterRecord:setPortrait(temp_list.portrait)
                army.leader.characterRecord:addAncillary(temp_list.anc[i])
            end
        end
    end
end

