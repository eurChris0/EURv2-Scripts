function eurConfed_func.eregionStoryCheck()
    if eregion_realms_start == 0 then
        if eur_turn_number > 2 then
            eur_eregion_active = true
            local sett = eur_sMap:getSettlement(CONFED_EVENTS[eur_localFactionName][1].sett)
            wait(jumpCamera, 0.5, sett.xCoord, sett.yCoord)
            M2TWEOP.scriptCommand("point_at_settlement", CONFED_EVENTS[eur_localFactionName][1].sett)
            stratmap.game.scriptCommand("reveal_tile", sett.xCoord.." "..sett.yCoord)
            stratmap.game.historicEvent(CONFED_EVENTS[eur_localFactionName][0].event, CONFED_EVENTS[eur_localFactionName][0].title, CONFED_EVENTS[eur_localFactionName][0].text)
            eregion_realms_start = 1
        end
    elseif eregion_realms_start == 1 then
        UIFlashStop()
        hideTiles()
        eregion_realms_start = 2
    elseif eregion_realms_start == 2 then
        local sett = eur_sMap:getSettlement(CONFED_EVENTS[eur_localFactionName][1].sett)
        if sett.ownerFaction.name == eur_localFactionName then
            stratmap.game.historicEvent(CONFED_EVENTS[eur_localFactionName][1].event, CONFED_EVENTS[eur_localFactionName][1].title, CONFED_EVENTS[eur_localFactionName][1].text)
            eregion_realms_sett_taken = eur_turn_number
            eregion_realms_start = 3
        end
    elseif eregion_realms_start == 3 then
        local sett = eur_sMap:getSettlement(CONFED_EVENTS[eur_localFactionName][1].capital)
        if sett.ownerFaction.name == eur_localFactionName then
            if eur_campaign:getFaction(CONFED_EVENTS[eur_localFactionName][1].enemy).numOfCharacters == 0 then
                eregion_realms_start = 7
                stratmap.game.historicEvent(CONFED_EVENTS[eur_localFactionName][3][4].event, CONFED_EVENTS[eur_localFactionName][3][4].title, CONFED_EVENTS[eur_localFactionName][3][4].text)
            elseif eur_turn_number == (eregion_realms_sett_taken+1) then
                stratmap.game.historicEvent(CONFED_EVENTS[eur_localFactionName][3][1].event, CONFED_EVENTS[eur_localFactionName][3][1].title, CONFED_EVENTS[eur_localFactionName][3][1].text)
            elseif eur_turn_number == (eregion_realms_sett_taken+4) then
                stratmap.game.historicEvent(CONFED_EVENTS[eur_localFactionName][3][2].event, CONFED_EVENTS[eur_localFactionName][3][2].title, CONFED_EVENTS[eur_localFactionName][3][2].text)
            elseif eur_turn_number == (eregion_realms_sett_taken+9) then
                stratmap.game.historicEvent(CONFED_EVENTS[eur_localFactionName][3][3].event, CONFED_EVENTS[eur_localFactionName][3][3].title, CONFED_EVENTS[eur_localFactionName][3][3].text)
            elseif eur_turn_number >= (eregion_realms_sett_taken+10) then
                CONFED_EVENTS[eur_localFactionName][3][0]()
                eregion_realms_start = 4
            end
        else
            if not cap_lost_event then
                stratmap.game.historicEvent(CONFED_EVENTS[eur_localFactionName][3][5].event, CONFED_EVENTS[eur_localFactionName][3][17].title, CONFED_EVENTS[eur_localFactionName][3][17].text)
                cap_lost_event = true
                eregion_realms_sett_taken = eregion_realms_sett_taken+1
            else
                eregion_realms_sett_taken = eregion_realms_sett_taken+1
            end
        end
    elseif eregion_realms_start == 4 then
        local sett = eur_sMap:getSettlement(CONFED_EVENTS[eur_localFactionName][1].capital)
        if eur_campaign:getFaction(CONFED_EVENTS[eur_localFactionName][1].enemy).numOfCharacters == 0 then
            eregion_realms_start = 7
            stratmap.game.historicEvent(CONFED_EVENTS[eur_localFactionName][3][4].event, CONFED_EVENTS[eur_localFactionName][3][4].title, CONFED_EVENTS[eur_localFactionName][3][4].text)
        elseif sett.ownerFaction.name == eur_localFactionName then
            stratmap.game.historicEvent(CONFED_EVENTS[eur_localFactionName][3][4].event, CONFED_EVENTS[eur_localFactionName][3][4].title, CONFED_EVENTS[eur_localFactionName][3][4].text)
            eregion_realms_start = 5
        else
            stratmap.game.historicEvent(CONFED_EVENTS[eur_localFactionName][3][5].event, CONFED_EVENTS[eur_localFactionName][3][18].title, CONFED_EVENTS[eur_localFactionName][3][18].text)
            eregion_realms_start = 6
        end
    elseif eregion_realms_start == 5 then
            --story continues
            eregion_realms_start = 7
    elseif eregion_realms_start == 6 then
        local sett = eur_sMap:getSettlement(CONFED_EVENTS[eur_localFactionName][1].capital)
        if sett.ownerFaction.name == eur_localFactionName then
            --story continues
            stratmap.game.historicEvent(CONFED_EVENTS[eur_localFactionName][3][4].event, CONFED_EVENTS[eur_localFactionName][3][4].title, CONFED_EVENTS[eur_localFactionName][3][4].text)
            eregion_realms_start = 7
        end
    elseif eregion_realms_start == 7 then
        if eur_turn_number > 30 then
            if eur_player_faction.settlementsNum > 5 then
                --maernil stuff
                local sett = eur_sMap:getSettlement(CONFED_EVENTS[eur_localFactionName][1].sett)
                sett.settlementStats.population = sett.settlementStats.population+500
                stratmap.game.historicEvent(CONFED_EVENTS[eur_localFactionName][3][5].event, CONFED_EVENTS[eur_localFactionName][3][5].title, CONFED_EVENTS[eur_localFactionName][3][5].text)
                eregion_realms_start = 8
            end
        end
    elseif eregion_realms_start == 8 then
        local sett = eur_sMap:getSettlement(CONFED_EVENTS[eur_localFactionName][1].sett)
        if sett.ownerFaction.name == eur_localFactionName then
            if sett.level >= 1 then
                local sett = eur_sMap:getSettlement(CONFED_EVENTS[eur_localFactionName][1].sett)
                sett.settlementStats.population = sett.settlementStats.population+1000
                stratmap.game.historicEvent(CONFED_EVENTS[eur_localFactionName][3][6].event, CONFED_EVENTS[eur_localFactionName][3][6].title, CONFED_EVENTS[eur_localFactionName][3][6].text)
                eregion_realms_start = 9
            end
        end
    elseif eregion_realms_start == 9 then
        local sett = eur_sMap:getSettlement(CONFED_EVENTS[eur_localFactionName][1].sett)
        if sett.ownerFaction.name == eur_localFactionName then
            if sett.level >= 2 then
                show_eregion_choice = true
            end
        end
    elseif eregion_realms_start == 10 then
        local faction = eur_campaign:getFaction("egypt")
        local settlement = eur_sMap:getSettlement("Forodwaith")
        if faction ~= nil then
            faction.factionRecord.canHorde = true
            if settlement.ownerFaction.name == "egypt" then
                settlement:changeOwner(eur_campaign:getFaction("slave"), false)
            end
        end
        if settlement.ownerFaction.name == "slave" then
            eregion_realms_start = 11
        end
    elseif eregion_realms_start == 11 then
        local faction = eur_campaign:getFaction("egypt")
        if faction.numOfCharacters == 0 then
            stratmap.game.historicEvent(CONFED_EVENTS[eur_localFactionName][3][11].event, CONFED_EVENTS[eur_localFactionName][3][11].title, CONFED_EVENTS[eur_localFactionName][3][11].text)
            eregion_realms_start = 12
        else
            for i = 0, eur_player_faction.settlementsNum - 1 do
                local sett = eur_player_faction:getSettlement(i)
                if sett ~= nil then
                    if sett.turmoil <= 5 then
                        sett.turmoil = 5
                    end
                end
            end
        end
    elseif eregion_realms_start == 12 then
        stratmap.game.historicEvent(CONFED_EVENTS[eur_localFactionName][3][12].event, CONFED_EVENTS[eur_localFactionName][3][12].title, CONFED_EVENTS[eur_localFactionName][3][12].text)
        eregion_realms_start = 18
        for i = 0, eur_player_faction.settlementsNum - 1 do
            local sett = eur_player_faction:getSettlement(i)
            if sett ~= nil then
                sett.turmoil = 0
            end
        end
    elseif eregion_realms_start == 13 then
        eregion_realms_start = 14
    elseif eregion_realms_start == 14 then
        if not misc_options.KD_Alliance_msg_1 then
            stratmap.game.historicEvent('EREGION_KHAZAD',EREGION_KD_NOTIF_TITLE,EREGION_KD_NOTIF_BODY)
            misc_options.KD_Alliance_msg_1 = true
        end
        --check lindon/imladris destroyed
        if (eur_campaign:getFaction("denmark").numOfCharacters == 0) or (eur_campaign:getFaction("saxons").numOfCharacters == 0) then
            if not checkCounter("kon_part_1") then
                M2TWEOP.setScriptCounter("kon_part_1", 1)
                stratmap.game.historicEvent('KOE_PART_1',KOE_PART_1_TITLE,KOE_PART_1_BODY)
            end
        end
        if (eur_campaign:getFaction("denmark").numOfCharacters == 0) and (eur_campaign:getFaction("saxons").numOfCharacters == 0) then
            if not checkCounter("kon_part_1") then
                M2TWEOP.setScriptCounter("kon_part_1", 1) 
                stratmap.game.historicEvent('KOE_PART_1',KOE_PART_1_TITLE,KOE_PART_1_BODY)
            end
