function eurReviveFaction(faction, sett)
    M2TWEOP.setScriptCounter("faction_spawning", 1)
    faction.isHorde = true
    eur_sMap:getSettlement(faction_revival[faction.name].capital):changeOwner(faction, false)
    if gen_units_list[faction.name] then
        if faction.capital then
            local army = eurSpawnArmy(faction.name, "random_name", faction.name.."revival_", "", true, 30, leaderheir_combi_list[faction.name].leader.unit, sett.xCoord, sett.yCoord, 7, 1, 0)
            if army then  
                for i = 1, 2 do
                    army:createUnit(gen_units_list[faction.name]["special"][1], 5, 0, 0)
                end
                for i = 1, 4 do
                    local rand = math.random(0, #gen_units_list[faction.name]["T3"]-1)
                    local new_unit = gen_units_list[faction.name]["T3"][rand]
                    army:createUnit(new_unit, 3, 0, 0)
                end
                for i = 1, 6 do
                        local rand = math.random(0, #gen_units_list[faction.name]["T2"]-1)
                        local new_unit = gen_units_list[faction.name]["T2"][rand]
                        army:createUnit(new_unit, 2, 0, 0)
                end
                for i = 1, 5 do
                    local rand = math.random(0, #gen_units_list[faction.name]["T1"]-1)
                    local new_unit = gen_units_list[faction.name]["T1"][rand]
                    army:createUnit(new_unit, 1, 0, 0)
                end
                if army.leader ~= nil then
                    local char = army.leader.characterRecord
                    if char ~= nil then
                        char:addTrait("AIBoost", 1)
                        char:addTrait("Locked", 1)
                        char:addTrait("BattleScarred", 2)
                        char:addTrait("FactionLeader", 1)
                        char:addTrait("GoodAmbusher", 1)
                        char:addTrait("GoodCommander", 3)
                        char:addTrait("Loyal", 1)
                        char:addTrait("LoyaltyStarter", 1)
                        char:addTrait("PietyStarter", 1)
                        char:addTrait("NaturalMilitarySkill", 1)
                        char:addTrait("NaturalManagementSkill", 1)
                    end
                end
                if not game_options.unlimited_revival then
                    faction_revival[faction.name].revived_already = true
                end
                stratmap.game.callConsole("add_money", "-" .. tostring(game_options.revival_cost))
            end
        end
    end
    --[[stratmap.game.scriptCommand("select_settlement", eur_player_faction.capital.name)
    if show_settUI then
        local recruitment_button = gameSTDUI.getUiElement("recruitment_button");
        if recruitment_button then 
            recruitment_button:execute();
        end
    end]]
    eur_campaign:setDipStance(dipRelType.alliance, eur_player_faction, faction)
    faction.isHorde = false
    revive_sett = nil
    revive_faction = nil
    M2TWEOP.setScriptCounter("faction_spawning", 0)
end

local revive_sett = nil
local revive_faction = nil

function reviveFactionButton()
    local settscroll = M2TWEOP.getSettlementInfoScroll();
    if settscroll ~= nil then
        local sett = settscroll.settlement
        if sett ~= nil then
            if capital_cities[sett.name] then
                local revivename = capital_cities[sett.name]
                if (eur_campaign:getFaction(revivename).numOfCharacters == 0) then 
                    ImGui.SetNextWindowPos(1700*eurbackgroundWindowSizeRight, 750*eurbackgroundWindowSizeBottom)
                    ImGui.SetNextWindowBgAlpha(0.0)
                    ImGui.SetNextWindowSize(65*eurbackgroundWindowSizeRight, 60*eurbackgroundWindowSizeBottom)
                    ImGui.Begin("revive_1", true, bit.bor(ImGuiWindowFlags.NoDecoration,ImGuiWindowFlags.NoBackground))
                    eurStyle("button_1", true)
                    if not faction_revival[revivename].revived_already then
                        if tableContains(faction_revival[revivename].revived_by, faction_disposition[eur_player_faction.name]) then
                            if revive_yes then
                                if eur_player_faction.capital.name ~= capital_cities[sett.name] then
                                    if eur_player_faction.money >= game_options.revival_cost then
                                        if ImGui.ImageButton("revive_button_1",revive_yes.img,50*eurbackgroundWindowSizeRight,48*eurbackgroundWindowSizeBottom) then
                                            if show_revive_choice == false then
                                                M2TWEOP.scriptCommand("play_sound_event", "STRAT_SCROLL_OPENS")
                                                show_revive_choice = true
                                                revive_sett = sett
                                                revive_faction = eur_campaign:getFaction(revivename)
                                            end
                                        end
                                        hoveredSimple("Revive faction: "..eur_campaign:getFaction(revivename).localizedName)
                                    else
                                        ImGui.Image(revive_no.img,50*eurbackgroundWindowSizeRight,48*eurbackgroundWindowSizeBottom) 
                                        hoveredSimple("Not enough gold, 15,000 required to revive faction: "..eur_campaign:getFaction(revivename).localizedName)
                                    end
                                else
                                    ImGui.Image(revive_no.img,50*eurbackgroundWindowSizeRight,48*eurbackgroundWindowSizeBottom) 
                                    hoveredSimple("Capital city, cannot revive faction: "..eur_campaign:getFaction(revivename).localizedName)
                                end
                            end
                        else
                            ImGui.Image(revive_no.img,50*eurbackgroundWindowSizeRight,48*eurbackgroundWindowSizeBottom) 
                            hoveredSimple("Unable to revive faction: "..eur_campaign:getFaction(revivename).localizedName..". Your faction cannot revive them.")
                        end
                    else
                        ImGui.Image(revive_no.img,50*eurbackgroundWindowSizeRight,48*eurbackgroundWindowSizeBottom) 
                        hoveredSimple("Unable to revive faction: "..eur_campaign:getFaction(revivename).localizedName..". This faction can no longer be revived.")
                    end
                    eurStyle("button_1", false)
                    ImGui.End()
                end
            end
        end
    end
end

function reviveFactionChoice()
    if revive_faction ~= nil then
        if revive_sett ~= nil then
            ImGui.SetNextWindowBgAlpha(0)
            ImGui.SetNextWindowSize(610*eurbackgroundWindowSizeRight, 310*eurbackgroundWindowSizeBottom)
            ImGui.SetNextWindowPos(660*eurbackgroundWindowSizeRight, 350*eurbackgroundWindowSizeBottom)
            ImGui.SetNextWindowFocus()
            ImGui.Begin("options_revivechoice_main", true, bit.bor(ImGuiWindowFlags.NoScrollWithMouse, ImGuiWindowFlags.NoDecoration, ImGuiWindowFlags.NoBackground))
            eurStyle("basic_4", true)
            if faction_bg[M2TWEOP.getCultureName(eur_player_faction.cultureID)] then
                ImGui.Image(faction_accept[M2TWEOP.getCultureName(eur_player_faction.cultureID)].img, 600*eurbackgroundWindowSizeRight, 300*eurbackgroundWindowSizeBottom)
            end
            ImGui.SetNextWindowPos(710*eurbackgroundWindowSizeRight, 380*eurbackgroundWindowSizeBottom)
            ImGui.SetNextWindowBgAlpha(0.6)
            ImGui.BeginChild("options_revivechoice Window##A15", 500*eurbackgroundWindowSizeRight, 250*eurbackgroundWindowSizeBottom, ImGuiChildFlags.FrameStyle)
            ImGui.SetWindowFontScale(1.1*eurbackgroundWindowSizeRight)
            ImGui.Dummy(0*eurbackgroundWindowSizeRight, 50*eurbackgroundWindowSizeRight)
            centeredText("Revive "..revive_faction.localizedName.."?",0)
            ImGui.NewLine()
            centeredText("Cost: 15,000 ", -20)
            ImGui.SameLine()
            ImGui.Image(coins.img,20*eurbackgroundWindowSizeRight,20*eurbackgroundWindowSizeBottom)
            ImGui.SetNextWindowPos(710*eurbackgroundWindowSizeRight, 510*eurbackgroundWindowSizeBottom)
            ImGui.SetNextWindowBgAlpha(0)
            ImGui.BeginChild("options_revivechoice Window##A16", 500*eurbackgroundWindowSizeRight, 100*eurbackgroundWindowSizeBottom, ImGuiChildFlags.FrameStyle)
            ImGui.SetWindowFontScale(1.1*eurbackgroundWindowSizeRight)
            if (centeredImageButton("Yes", 80, 50, -40)) then
                show_revive_choice = false
                eurReviveFaction(revive_faction, revive_sett)
                M2TWEOP.scriptCommand("play_sound_event", "BUTTON_DOWN")
            end
            ImGui.SameLine()
            if (centeredImageButton("No", 80, 50, 40)) then
                show_revive_choice = false
                M2TWEOP.scriptCommand("play_sound_event", "BUTTON_DOWN")
            end

            ImGui.EndChild()
            ImGui.EndChild()
            eurStyle("basic_4", false)
            ImGui.End()
        end
    end
