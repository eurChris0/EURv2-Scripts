function countUnitsTrained(factname, unitname)
    if factname == "Independent Realms" then return end
    if not stats_units_trained[factname] then
        stats_units_trained[factname] = {}
    end
    if not stats_units_trained[factname][unitname] then
        stats_units_trained[factname][unitname] = 1
    else
        stats_units_trained[factname][unitname] = stats_units_trained[factname][unitname] + 1
    end
    if not stats_units_trained_total[factname] then
        stats_units_trained_total[factname] = 1
    else
        stats_units_trained_total[factname] = stats_units_trained_total[factname] + 1
    end
    stats_per_turn_table[factname][eur_turn_number+1]["Units Trained"] = stats_per_turn_table[factname][eur_turn_number+1]["Units Trained"] + 1
    if stats_per_turn_table[factname][eur_turn_number+1]["Units Trained"] > extents_entries["Units Trained"] then
        extents_entries["Units Trained"] = stats_per_turn_table[factname][eur_turn_number+1]["Units Trained"]
    end
end

