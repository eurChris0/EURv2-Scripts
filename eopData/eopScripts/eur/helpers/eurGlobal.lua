function serialize(val)
    local t = type(val)
    if t == "number" or t == "boolean" then
        return tostring(val)
    elseif t == "string" then
        return string.format("%q", val)
    elseif t == "table" then
        local s = "{"
        for k, v in pairs(val) do
            local key = type(k) == "string" and k or "[" .. k .. "]"
            s = s .. key .. " = " .. serialize(v) .. ", "
        end
        return s .. "}"
    end
end

