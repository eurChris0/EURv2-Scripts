function load_submod_config()
    local path = M2TWEOP.getModPath()
    local filename = path .. "\\chris_submod.ini"
    local current_section = nil
    
    for line in io.lines(filename) do
        line = line:match("^%s*(.-)%s*$")
        
        if line == "" or line:match("^[;#]") then
            -- skip

        elseif line:match("^%[(.+)%]$") then
            current_section = line:match("^%[(.+)%]$")

        elseif line:match("^(.-)%s*=%s*(.+)$") and current_section == "main" then
            local key, value = line:match("^(.-)%s*=%s*(.+)$")
            
            if value == "True" then value = true
            elseif value == "False" then value = false
            elseif tonumber(value) then value = tonumber(value)
            end

            if key == "lite_mode" then cutdown_setts = value
            elseif key == "enabled" then chris_stuff = value
            elseif key == "extra_settlements" then add_setts = value
            elseif key == "player_expansion" then setts_player_expansion = value
            elseif key == "extra_units" then options_extraBGunits = value
            elseif key == "extra_buildings" then options_extrabu = value
            end
        end
    end
end

