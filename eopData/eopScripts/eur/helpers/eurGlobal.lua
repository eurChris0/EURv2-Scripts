        for i = 1, #mod_general_units_list do
            local eduEntry = M2TWEOPDU.getEduEntryByType(mod_general_units_list[i].name)
            if eduEntry ~= nil then
                if eduEntry.soldierCount == mod_general_units_list[i].size then
