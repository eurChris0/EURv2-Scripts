last_random = {}

function random_no_repeat(min, max)
    -- guard: invalid range
    if min == nil then return 1 end
    if max == nil then return 1 end
    if min > max then return 1 end
    -- guard: only one option
    if min == max then return min end
    -- guard: only two options, just alternate
    if max - min == 1 then
        local result = (last_random[min] == min) and max or min
        last_random[min] = result
        return result
    end

    local n
    local attempts = 0
    repeat
        n = math.random(min, max)
        attempts = attempts + 1
        if attempts > 10 then return n end  -- bail out after 10
    until n ~= last_random[min]

    last_random[min] = n
    return n
end

function randomChance(percent) -- returns true a given percentage of calls
    if percent == 0 then return false end
    return percent >= math.random(1,100)   -- 1 succeeds 1%, 50 succeeds 50%,
end

