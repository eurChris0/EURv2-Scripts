    local sorted1, sorted2 = {}, {}
    for i, idx in ipairs(indices) do
        sorted1[i] = t1[idx]
        sorted2[i] = t2[idx]
    end

    for i = 1, #sorted1 do
        t1[i] = sorted1[i]
        t2[i] = sorted2[i]
    end
end

function shuffle(tbl)
    for i = #tbl, 2, -1 do
      local j = math.random(i)
      tbl[i], tbl[j] = tbl[j], tbl[i]
    end
    return tbl
  end

function printTable( t )
 
    local printTable_cache = {}
 
    local function sub_printTable( t, indent )
 
        if ( printTable_cache[tostring(t)] ) then
            print( indent .. "*" .. tostring(t) )
        else
            printTable_cache[tostring(t)] = true
            if ( type( t ) == "table" ) then
                for pos,val in pairs( t ) do
                    if ( type(val) == "table" ) then
                        print( indent .. "[" .. pos .. "] => " .. tostring( t ).. " {" )
                        sub_printTable( val, indent .. string.rep( " ", string.len(pos)+8 ) )
                        print( indent .. string.rep( " ", string.len(pos)+6 ) .. "}" )
                    elseif ( type(val) == "string" ) then
                        print( indent .. "[" .. pos .. '] => "' .. val .. '"' )
                    else
                        print( indent .. "[" .. pos .. "] => " .. tostring(val) )
                    end
                end
            else
                print( indent..tostring(t) )
            end
        end
    end
 
    if ( type(t) == "table" ) then
        print( tostring(t) .. " {" )
        sub_printTable( t, "  " )
        print( "}" )
    else
        sub_printTable( t, "  " )
    end
end


function reorder_top_cost_items(list, count_to_move)
    -- Find max index for zero-based table
    local max_index = -1
    for k in pairs(list) do
        if k > max_index then max_index = k end
    end

    -- Build array with original indexes
    local items = {}
    for i = 0, max_index do
        if list[i] then
            table.insert(items, {index = i, item = list[i]})
        end
    end

    -- Sort by cost descending, tie-breaker: lower index first (reversed logic)
    table.sort(items, function(a, b)
        if a.item.cost ~= b.item.cost then
            return a.item.cost > b.item.cost
        else
            return a.index < b.index  -- reversed tie breaker: lower index first
        end
    end)

    -- Mark top N entries to move
    local top_set = {}
    for i = 1, math.min(count_to_move, #items) do
        top_set[items[i].index] = true
    end

    -- Separate front and back lists
    local front, back = {}, {}
    for i = 0, max_index do
        if list[i] then
            if top_set[i] then
                table.insert(back, {index = i, item = list[i]})
            else
                table.insert(front, {index = i, item = list[i]})
            end
        end
    end

    -- Sort back by reverse original index
    table.sort(back, function(a, b)
        return a.index > b.index
    end)

    -- Rebuild final list
    local result = {}
    local i = 0
    for _, entry in ipairs(front) do
        result[i] = entry.item
        i = i + 1
    end
    for _, entry in ipairs(back) do
        result[i] = entry.item
        i = i + 1
    end

    return result
end

function reorder_top_cost_free_upkeep_only(list, count_to_move, free_upkeep_index)
    -- Helper to check if eduindex is in free_upkeep_index
    local function is_in_free_upkeep(edu)
        for _, v in ipairs(free_upkeep_index) do
            if v == edu then return true end
        end
        return false
    end

    -- Tag each item with its original index
    local items = {}
    for i = 0, #list do
        table.insert(items, {index = i, item = list[i]})
    end

    -- Filter for only eligible free upkeep entries (eduindex match AND free = true)
    local eligible = {}
    for _, entry in ipairs(items) do
        if entry.item.free and is_in_free_upkeep(entry.item.eduindex) then
            table.insert(eligible, entry)
        end
    end

    -- Sort eligible by cost descending, tiebreaker: lowest index first
    table.sort(eligible, function(a, b)
        if a.item.cost ~= b.item.cost then
            return a.item.cost > b.item.cost
        else
            return a.index < b.index  -- lower index chosen first
        end
    end)

    -- Select up to count_to_move
    local top_set = {}
    for i = 1, math.min(count_to_move, #eligible) do
        top_set[eligible[i].index] = true
    end

    -- Separate into front and back (back = to be moved)
    local front, back = {}, {}
    for _, entry in ipairs(items) do
        if top_set[entry.index] then
            table.insert(back, entry)
        else
            table.insert(front, entry)
        end
    end

    -- Sort back by reverse original index (to preserve visual back placement)
    table.sort(back, function(a, b)
        return a.index > b.index
    end)

    -- Rebuild final list
    local result = {}
    local i = 0
    for _, entry in ipairs(front) do
        result[i] = entry.item
        i = i + 1
    end
    for _, entry in ipairs(back) do
        result[i] = entry.item
        i = i + 1
    end

    return result
end

